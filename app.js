function checkAnswer() {
	const radioCheck = document.querySelector('input[type="radio"]:checked') //–ø–æ–ª—É—á–∞–µ–º –≤—ã–±—Ä–∞–Ω–Ω—É—é —Ä–∞–¥–∏–æ–∫–Ω–æ–ø–∫—É

	if (!radioCheck) {
		btn.blur();
		return //–ø–æ—Ä–æ—Å—Ç–æ –≤—ã—Ö–æ–¥–∏–º –∏–∑ —Ñ—É–∫—Ü–∏–∏
	}

	const userAnswer = Number(radioCheck.value);

	if (userAnswer === questions[quastionIndex].correct) {
		score++
	}

	if (quastionIndex !== questions.length - 1) {
		quastionIndex++
		clear();
		showQuastion()
	}
	else {
		clear();
		results();
	}
}

